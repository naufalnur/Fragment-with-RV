package com.example.fragmentwithrv;

public class ShoesModel {
    private int shoesImage;
    private String shoesName;
    private String shoesDetail;

    public String getShoesName() {
        return shoesName;
    }

    public void setShoesName(String shoesName) {
        this.shoesName = shoesName;
    }

    public String getShoesDetail() {
        return shoesDetail;
    }

    public void setShoesDetail(String shoesDetail) {
        this.shoesDetail = shoesDetail;
    }

    public int getShoesImage() {
        return shoesImage;
    }

    public void setShoesImage(int shoesImage) {
        this.shoesImage = shoesImage;
    }
}
